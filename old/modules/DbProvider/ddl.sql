create table EMAIL_TYPE
(
    ID   INTEGER not null
        constraint EMAIL_TYPE_pk
            primary key autoincrement,
    NAME TEXT
);

create table REP
(
    ID   INTEGER
        constraint REP_pk
            primary key autoincrement,
    NAME TEXT
);

create table CLIENT
(
    ID     INTEGER
        constraint CLIENT_pk
            primary key,
    REP_ID INTEGER
        constraint CLIENT_REP_fk
            references REP,
    NAME   INTEGER
);

create table CLIENT_EMAIL
(
    ID        INTEGER
        constraint EMAIL_pk
            primary key autoincrement,
    TYPE_ID   INTEGER
        constraint CLIENT_EMAIL_EMAIL_TYPE_ID_fk
            references EMAIL_TYPE,
    CLIENT_ID INTEGER
        constraint EMAIL_CLIENT_CLIENT_fk
            references CLIENT,
    ADDRES    TEXT
);

create table REP_EMAIL
(
    ID      INTEGER
        constraint REP_EMAIL_pk
            primary key autoincrement,
    TYPE_ID INTEGER
        constraint REP_EMAIL_EMAIL_TYPE_null_fk
            references EMAIL_TYPE,
    REP_ID  INTEGER
        constraint REP_EMAIL_REP_null_fk
            references REP,
    ADDRESS TEXT
);

create table RESULT
(
    ID   INTEGER
        constraint RESULT_pk
            primary key autoincrement,
    NAME TEXT
);

create table CLIENT_RESULT
(
    ID        INTEGER
        constraint CLIENT_RESULT_pk
            primary key autoincrement,
    CLIENT_ID INTEGER
        constraint CLIENT_RESULT_CLIENT_CLIENT_fk
            references CLIENT,
    RESULT_ID INTEGER
        constraint CLIENT_RESULT_RESULT_null_fk
            references RESULT
);

